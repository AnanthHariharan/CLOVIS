public class User {
    public static String name;
    public static int age;

    // Age Methods
    public static int getAge() {
        return age;
    }
    public static void setAge(int givenAge) {
        age = givenAge;
    }

    // Name Methods
    public static String getName() {
        return name;
    }
    public static void setName(String givenName) {
        name = givenName;
    }
}


